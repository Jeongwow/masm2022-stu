MAIN SEGMENT
ASSUME CS:MAIN, DS:MAIN

    MOV AX,CS
    MOV DS,AX

    MOV SMALL,0
    MOV SI,OFFSET VAL

    MOV CX,0
CMPARE:
    CMP CX,3
    JE PRINT

    MOV AL,SMALL
    CMP AL,[SI]
    JB CHANGE

COMBACK:
    INC CX
    INC SI
    JMP CMPARE

CHANGE:
    MOV AL,SMALL
    MOV AL,[SI]
    JMP COMBACK

PRINT:
    MOV DL,SMALL
    ADD DL,'0'
    MOV AH,2
    INT 21H
    JMP EXIT

EXIT:
    MOV AH,4CH
    INT 21H


SMALL DB ?
VAL DB 7,3,4

MAIN ENDS
END